// Math function and Problems

// Math.round or Math.absolute
console.log(Math.abs(-10));  //-ve to +ve

// Math.ceil()
console.log(Math.ceil(10.3)); // roof --> 11
 
// Math.floor()
console.log(Math.floor(44.4)); //44 

// Math.round()
console.log(Math.round(13.5)); // 14 if greater than and equal to .5
console.log(Math.round(13.4)); //13

// Math trunc
console.log(Math.trunc(10.737838)); //10 calculation and sales

// Math min
console.log(Math.min(3,4,8,339,-1,0)); // -1

// Math.max
console.log(Math.max(19,44,67,89,23));

// Math.random
console.log(Math.random());

// toFixed()
let price = 99.837838;
console.log(price.toFixed(3)); //output as string

// sqrt()
let num = 441;
console.log(Math.sqrt(num));

// pow()
let n = 2;
console.log(Math.pow(n,5));